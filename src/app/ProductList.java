    private void setupTableSorter() {
        DefaultTableModel model = (DefaultTableModel) tblPilihProduct.getModel();
        rowSorter = new TableRowSorter<>(model);
        tblPilihProduct.setRowSorter(rowSorter);
    }

    private void filterTable() {
        String text = txtSearch.getText();
        if (text.trim().length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + text));
        }
    }

    private void loadAllProducts() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblPilihProduct.getModel();
            model.setRowCount(0);

            String sql = "SELECT p.id, p.kode_barang, p.nama, p.harga, p.stok, c.nama_category, b.nama_brand " +
                         "FROM products p " +
                         "LEFT JOIN categories c ON p.category_id = c.id " +
                         "LEFT JOIN brands b ON p.brand_id = b.id";

            pst = conn.prepareStatement(sql);
            rslt = pst.executeQuery();

            while (rslt.next()) {
                Object[] row = new Object[]{
                    rslt.getInt("id"),
                    rslt.getString("kode_barang"),
                    rslt.getString("nama"),
                    rslt.getInt("harga"),
                    rslt.getInt("stok"),
                    rslt.getString("nama_category"),
                    rslt.getString("nama_brand")
                };
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductList.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


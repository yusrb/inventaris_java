    private void btnPilihProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihProductActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblPilihProduct.getSelectedRow();

        if (selectedRow < 0) {
            setAlwaysOnTop(false);
            JOptionPane.showMessageDialog(this, "Pilih produk terlebih dahulu!");
            setAlwaysOnTop(true);
            return;
        }

        int modelRow = tblPilihProduct.convertRowIndexToModel(selectedRow);
        String kodeProduk = tblPilihProduct.getModel().getValueAt(modelRow, 1).toString();

        if (cashier_page != null) {
            int stokTersedia = Integer.parseInt(tblPilihProduct.getModel().getValueAt(modelRow, 4).toString());
            int jumlahDiKeranjang = cashier_page.getJumlahProdukDalamKeranjang(kodeProduk);

            if (jumlahDiKeranjang >= stokTersedia) {
                this.dispose();
                JOptionPane.showMessageDialog(this, "Stok produk tidak mencukupi untuk ditambahkan lagi.");
                return;
            }

            cashier_page.loadProductToCart(kodeProduk);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Cashier tidak ditemukan.");
        }
    }//GEN-LAST:event_btnPilihProductActionPerformed


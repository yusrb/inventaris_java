    private void setupTableSorter() {
        DefaultTableModel model = (DefaultTableModel) tblPilihSupplier.getModel();
        rowSorter = new TableRowSorter<>(model);
        tblPilihSupplier.setRowSorter(rowSorter);
    }

    private void filterTable() {
        String text = txtSearch.getText();
        if (text.trim().length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + text));
        }
    }

    private void loadAllSuppliers() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblPilihSupplier.getModel();
            model.setRowCount(0);

            String sql = "SELECT id, nama_supplier, kontak, alamat FROM suppliers";
            pst = conn.prepareStatement(sql);
            rslt = pst.executeQuery();

            while (rslt.next()) {
                Object[] row = new Object[]{
                    rslt.getInt("id"),
                    rslt.getString("nama_supplier"),
                    rslt.getString("kontak"),
                    rslt.getString("alamat")
                };
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(SupplierList.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


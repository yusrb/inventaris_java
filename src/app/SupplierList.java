    private void btnPilihSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihSupplierActionPerformed
        // TODO add your handling code here:
       
        System.out.println(barangMasuk);
        
        int selectedRow = tblPilihSupplier.getSelectedRow();
        if (selectedRow != -1) {
            int modelRow = tblPilihSupplier.convertRowIndexToModel(selectedRow);

            int supplierID = Integer.parseInt(tblPilihSupplier.getModel().getValueAt(modelRow, 0).toString());
            String namaSupplier = tblPilihSupplier.getModel().getValueAt(modelRow, 1).toString();
            String kontak = tblPilihSupplier.getModel().getValueAt(modelRow, 2).toString();
            String alamat = tblPilihSupplier.getModel().getValueAt(modelRow, 3).toString();

            BarangMasukForm form = new BarangMasukForm(barangMasuk, usernameForPage, levelForPage,
                                                       supplierID, namaSupplier, kontak, alamat);
            form.setVisible(true);
    
            if (barangMasuk != null) {
                barangMasuk.dispose();
            }

            this.dispose();
        }
    }//GEN-LAST:event_btnPilihSupplierActionPerformed

